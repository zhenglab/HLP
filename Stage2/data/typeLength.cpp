#include<iostream>
using namespace std;
int main()
{
  cout<<"The size of int is "<<sizeof(int)<<" Bytes."<<endl;
  cout<<"The size of short int is "<<sizeof(short)<<" Bytes."<<endl;
  cout<<"The size of long int is "<<sizeof(long)<<" Bytes."<<endl;
  cout<<"The size of float is "<<sizeof(float)<<" Bytes."<<endl;
  cout<<"The size of double is "<<sizeof(double)<<" Bytes."<<endl;
  cout<<"The size of long double is "<<sizeof(long double)<<" Bytes."<<endl;
  cout<<"The size of char is "<<sizeof(char)<<" Byte(s)."<<endl;
  cout<<"The size of bool is "<<sizeof(bool)<<" Byte(s)."<<endl;
  return 0;
}
